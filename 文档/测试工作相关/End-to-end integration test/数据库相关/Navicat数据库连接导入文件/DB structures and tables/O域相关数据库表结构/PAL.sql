-- ----------------------------
-- Procedure structure for ACCEPT_ASAP_RESULT
-- ----------------------------
CREATE OR REPLACE procedure "NONPHS_AA"."ACCEPT_ASAP_RESULT"(
       VI_CORRID in ASAP_WORKORDER_ITEM.CORRID%type,
       VI_WORK_ORDER_NUMBER in ASAP_WORKORDER_ITEM.WORK_ORDER_NUMBER%type,
       VI_EXCUTIONMODE IN Asap_Workorder_Request.Excutionmode%TYPE,
       NERR OUT number,
       SERR OUT varchar2) IS

       v_count number;
       v_order_seq_id asap_workorder_request.order_seq_id%TYPE;
       v_hist_seq_id  asap_workorder_request.hist_seq_id%TYPE;
       v_crm_order_no asap_workorder_request.crm_order_number%TYPE;

       v_asap_response asap_workorder_response.work_order_return%TYPE;
       PRAGMA AUTONOMOUS_TRANSACTION;
begin
  nerr:=0;
  serr:='';

  IF VI_CORRID IS NOT NULL THEN
     v_asap_response:='<P6_Response xmlns="http://java.sun.com/products/oss/xml/ServiceActivation" xmlns:fo="http://www.w3.org/1999/XSL/Format" xmlns:co="http://java.sun.com/products/oss/xml/Common" xmlns:mslv-sa="http://www.metasolv.com/oss/ServiceActivation/2003" xmlns:mslv-srt-sa="http://www.metasolv.com/oss/SRTServiceActivation/2005" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" xmlns:sa="http://java.sun.com/products/oss/xml/ServiceActivation">'
                       || '<p_order_type/>'
                       || '<p_order_no>' || VI_WORK_ORDER_NUMBER || '</p_order_no>'
                       || '<p_activation_result>0</p_activation_result>'
                       || '<p_description>asap_manual_return</p_description>'
                       || '</P6_Response>';

     SELECT COUNT(*) INTO v_count FROM asap_workorder_request r,asap_workorder_item i
        WHERE i.corrid=vi_corrid AND r.order_seq_id=i.order_seq_id AND r.hist_seq_id=i.hist_seq_id AND r.target=i.target;

     IF v_count>0 THEN
        SELECT r.order_seq_id,r.hist_seq_id,r.crm_order_number INTO v_order_seq_id,v_hist_seq_id,v_crm_order_no FROM asap_workorder_request r,asap_workorder_item i
           WHERE i.corrid=vi_corrid AND r.order_seq_id=i.order_seq_id AND r.hist_seq_id=i.hist_seq_id AND r.target=i.target;

        INSERT INTO asap_manual_return (SEQID,CORRID,WORK_ORDER_NUMBER,EXCUTIONMODE,RECEIVE_TIME,ORDER_SEQ_ID,HIST_SEQ_ID,CRM_ORDER_NUMBER)
           VALUES (seq_asap_manual_return.nextval,VI_CORRID,VI_WORK_ORDER_NUMBER,VI_EXCUTIONMODE,SYSDATE,v_order_seq_id,v_hist_seq_id,v_crm_order_no);
        INSERT INTO asap_workorder_response(seqid,corrid,p6corrid,need_process,work_order_return,state,receive_time,oss_event_type,oss_order_current_state)
           VALUES(SEQ_ACTIVATION_RESPONSE.Nextval,VI_CORRID,VI_CORRID,'1',v_asap_response,1,SYSDATE,'javax.oss.order.OrderStateChangeEvent','open.running.suspended.failed');
     ELSE
        INSERT INTO asap_manual_return (SEQID,CORRID,WORK_ORDER_NUMBER,EXCUTIONMODE,RECEIVE_TIME)
           VALUES (seq_asap_manual_return.nextval,VI_CORRID,VI_WORK_ORDER_NUMBER,VI_EXCUTIONMODE,SYSDATE);
     END IF;
  ELSE
     INSERT INTO asap_manual_return (SEQID,CORRID,WORK_ORDER_NUMBER,EXCUTIONMODE,RECEIVE_TIME)
        VALUES (seq_asap_manual_return.nextval,VI_CORRID,VI_WORK_ORDER_NUMBER,VI_EXCUTIONMODE,SYSDATE);
  END IF;

  commit;
  EXCEPTION
      WHEN OTHERS
      THEN
         nerr := SQLCODE;
         serr := '其他错误!' || SUBSTR (SQLERRM, 1, 50);
         ROLLBACK;
end ACCEPT_ASAP_RESULT;

