CREATE OR REPLACE PROCEDURE "NONPHS_AA"."CLEAN_UP_WO"(VI_ORDER_SEQ_ID IN NUMBER,
                                        --VI_TARGET       IN VARCHAR2,
                                        NERR            IN OUT NUMBER,
                                        SERR            IN OUT VARCHAR2) IS
BEGIN
  IF VI_ORDER_SEQ_ID IS NULL THEN
    NERR := 3000;
    SERR := SERR || '|' || '必须输入VI_ORDER_SEQ_ID';
    RETURN;
  END IF;


  --清理RA工单
  BEGIN
    DELETE FROM RA_WORKORDER_REQUEST T
     WHERE T.ORDER_SEQ_ID = VI_ORDER_SEQ_ID;
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理ASAP工单
  BEGIN
    DELETE FROM ASAP_WORKORDER_REQUEST T
     WHERE T.ORDER_SEQ_ID = VI_ORDER_SEQ_ID;
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理PAI工单
  BEGIN
      DELETE FROM PAI_WORKORDER_REQUEST T
       WHERE T.ORDER_SEQ_ID = VI_ORDER_SEQ_ID;
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理CRM反馈消息
  BEGIN
      DELETE FROM P6_CRM_NOTIFICATION T
       WHERE T.ORDER_SEQ_ID = VI_ORDER_SEQ_ID
         AND T.STATE IN ('1', '2', '5');
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理服务配置结果
  BEGIN
      DELETE FROM INT_RES_CONFIG_RESULT_NEW T
       WHERE T.ORDER_SEQ_NO = VI_ORDER_SEQ_ID;
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理IBP_P6_TASK_ID
  BEGIN
      DELETE FROM IBP_P6_TASK_ID T
       WHERE T.P6_ORDER_ID = TO_CHAR(VI_ORDER_SEQ_ID);
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;

  --清理JT_DATA_GATHER
  BEGIN
    DELETE FROM JT_DATA_GATHER T
     WHERE T.ORDER_SEQ_ID = VI_ORDER_SEQ_ID;
    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      NERR := SQLCODE;
      SERR := SERR || '|' || '其他错误:' || SQLERRM;
      ROLLBACK;
  END;
END CLEAN_UP_WO;

